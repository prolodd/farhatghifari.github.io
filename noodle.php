  <h4> Ingredients </h4>
  <ul>
        <li>1 packet of Indomie noodles (any flavor of your choice)</li>
        <li>2 Water for boiling</li>
        <li>2 eggs, lightly beaten</li>
        <li>Seasoning sachets included in the Indomie packet (seasoning powder, seasoning oil, chili sauce)</li>
        <li>Optional toppings (fried egg, sliced vegetables, cooked meat or seafood, chopped green onions)</li>
    </ul>

    <h4> Instructions </h4>
    <ol>
        <li>Bring a pot of water to a boil.</li>
        <li>Open the packet of Indomie noodles and take out the seasoning sachets.</li>
        <li>Add the noodles to the boiling water and cook for about 2-3 minutes, or until they are soft but still slightly firm (al dente).</li>
        <li>Add the mixed vegetables to the skillet and stir-fry for 3-4 minutes until they're tender.</li>
        <li>While the noodles are cooking, empty the seasoning powder and oil sachets into a bowl.</li>
        <li>Once the noodles are cooked, drain them in a colander and then transfer them to the bowl with the seasoning powder and oil.</li>
        <li>Mix the noodles with the seasoning powder and oil until they are evenly coated.</li>
        <li>If you like spicy noodles, you can also add the chili sauce provided in the packet.</li>
        <li>If desired, you can also add any optional toppings such as a fried egg, sliced vegetables, cooked meat or seafood, or chopped green onions.</li>
        <li>Mix everything together and serve hot.</li>
    </ol>
