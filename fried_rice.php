  <h4> Ingredients </h4>
  <ul>
        <li>3 cups cooked rice (preferably leftover rice, as it's drier and works better for fried rice)</li>
        <li>2 tablespoons vegetable oil</li>
        <li>2 eggs, lightly beaten</li>
        <li>1 cup mixed vegetables (such as peas, carrots, corn, and chopped bell peppers)</li>
        <li>2 cloves garlic, minced</li>
        <li>2 green onions, thinly sliced</li>
        <li>3 tablespoons soy sauce</li>
        <li>1 tablespoon oyster sauce (optional)</li>
        <li>Salt and pepper to taste</li>
        <li>Optional additions: diced chicken, shrimp, or tofu for added protein</li>
    </ul>

    <h4> Instructions </h4>
    <ol>
        <li>Heat 1 tablespoon of vegetable oil in a large skillet or wok over medium-high heat.</li>
        <li>Add the beaten eggs to the skillet and scramble them until they're fully cooked. Remove them from the skillet and set aside.</li>
        <li>In the same skillet, add the remaining tablespoon of vegetable oil. Add the minced garlic and cook for about 30 seconds until fragrant.</li>
        <li>Add the mixed vegetables to the skillet and stir-fry for 3-4 minutes until they're tender.</li>
        <li>If you're adding any protein (such as diced chicken, shrimp, or tofu), add it to the skillet and cook until it's fully cooked through.</li>
        <li>Add the cooked rice to the skillet and stir-fry everything together, breaking up any clumps of rice.</li>
        <li>Pour the soy sauce (and oyster sauce if using) over the rice mixture and stir well to combine, ensuring that the sauce coats all of the rice evenly.</li>
        <li>Add the scrambled eggs back into the skillet and stir-fry everything together for another 2-3 minutes until everything is heated through.</li>
        <li>Taste and adjust the seasoning with salt and pepper if needed.</li>
        <li>Garnish the fried rice with sliced green onions before serving.</li>
    </ol>
