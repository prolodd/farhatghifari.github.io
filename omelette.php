  <h4> Ingredients </h4>
  <ul>
        <li>3 large eggs</li>
        <li>1 tablespoon butter or oil</li>
        <li>2 Salt and pepper to taste</li>
        <li>Optional fillings (such as cheese, vegetables, cooked meat, herbs)</li>
    </ul>

    <h4> Instructions </h4>
    <ol>
        <li>Crack the eggs into a bowl and season with salt and pepper. Use a fork or whisk to beat the eggs until well combined and slightly frothy.</li>
        <li>Heat the butter or oil in a non-stick skillet over medium heat until melted and hot.</li>
        <li>Pour the beaten eggs into the skillet, tilting the pan to spread them evenly.</li>
        <li>Let the eggs cook undisturbed for about 1-2 minutes until the edges start to set.</li>
        <li>Use a spatula to gently lift the edges of the omelette and tilt the skillet to let the uncooked eggs flow to the edges.</li>
        <li>Continue cooking for another 1-2 minutes until the omelette is mostly set but still slightly runny on top.</li>
        <li>If using any fillings, sprinkle them evenly over one half of the omelette.</li>
        <li>Use a spatula to fold the other half of the omelette over the filling, creating a half-moon shape.</li>
        <li>Cook for another 1-2 minutes until the omelette is cooked through and the fillings are heated.</li>
        <li>Slide the omelette onto a plate and serve hot.</li>
    </ol>
